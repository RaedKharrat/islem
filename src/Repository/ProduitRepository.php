class ProduitRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Produit::class);
    }


    // src/Repository/ProduitRepository.php

public function findAllAscending(string $column)
{
    return $this->createQueryBuilder('p') // Use 'p' as the alias for the Produit entity
        ->orderBy('p.' . $column, 'ASC') // Order by the field name (prix in this case)
        ->getQuery()
        ->getResult();
}

public function findAllDescending(string $column)
{
    return $this->createQueryBuilder('p')
        ->orderBy('p.' . $column, 'DESC')
        ->getQuery()
        ->getResult();
}


